
public class SwitchStatments {
	public static void main(String [] args){
		int age;
		age = 26;
		
		switch (age){
		case 1: 
			System.out.println("not the same age");
			break;
		case 25: 
			System.out.println("it is  the same age");
			break;
		default:  	
			System.out.println("This is the default value");
			break;
		}
	}
}
